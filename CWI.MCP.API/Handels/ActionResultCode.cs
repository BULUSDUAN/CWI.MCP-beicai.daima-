namespace CWI.MCP.API.Handels
{
    /// <summary>
    /// Action返回结果枚举
    /// </summary>
    public enum ActionResultCode
    {
        /// <summary>
        /// 执行失败
        /// </summary>
        Failed = 0,

        /// <summary>
        /// 执行成功
        /// </summary>
        Success = 1,

        /// <summary>
        /// 内部错误
        /// </summary>
        InnerError = 2,

        /// <summary>
        /// 未登录。
        /// </summary>
        Unauthorized = 3,

        /// <summary>
        /// 需要客户端确认
        /// </summary>
        Confirm = 4,

        /// <summary>
        /// 系统未知异常
        /// </summary>
        UnknownError = 5,

        /// <summary>
        /// 重复提交
        /// </summary>
        RepeatSubmit = 99
    }
}