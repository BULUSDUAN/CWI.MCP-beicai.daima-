using Evt.Framework.Mvc;

namespace CWI.MCP.Models.ViewModels.MCP.OPEN
{
    /// <summary>
    /// 内容摘要：模板对应的参数视图Model
    /// 编码作者：ZLP
    /// 编码时间：2016-7-14
    /// </summary>
    public class DevExpressTemplateParamsViewModel : ViewModel
    {
        /// <summary>
        /// 模板ID
        /// </summary>
        public string TemplateId { set; get; }

        /// <summary>
        /// 参数序号
        /// </summary>
        public int ParamIndex { set; get; }

        /// <summary>
        /// 参数代码
        /// </summary>
        public string ParamCode { set; get; }

        /// <summary>
        /// 参数描述
        /// </summary>
        public string ParamDesc { set; get; }

        /// <summary>
        /// 参数值最大字符数:0-不受限制
        /// </summary>
        public int ParamMaxLen { set; get; }

        /// <summary>
        /// 参数X轴位置
        /// </summary>
        public decimal LocX { set; get; }

        /// <summary>
        /// 参数Y轴位置
        /// </summary>
        public decimal LocY { set; get; }

        /// <summary>
        /// 是否必填项:0-非必填,1-必填
        /// </summary>
        public int IsNeed { set; get; }
    }
}
