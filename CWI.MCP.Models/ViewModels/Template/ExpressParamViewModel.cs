using CWI.MCP.Common;
using Evt.Framework.Mvc;
using System.ComponentModel.DataAnnotations;

namespace CWI.MCP.Models
{
    /// <summary>
    /// 快递参数坐标
    /// </summary>
    public class ExpressParamViewModel : ViewModel
    {
        /// <summary>
        /// 参数编码
        /// </summary>
        public string param_code { get; set; }

        /// <summary>
        /// X轴坐标
        /// </summary>
        public decimal loc_x { get; set; }

        /// <summary>
        /// Y轴坐标
        /// </summary>
        public decimal loc_y { get; set; }

        /// <summary>
        /// 参数类型
        /// </summary>
        public int param_type { get; set; }

        /// <summary>
        /// 是否必填项
        /// </summary>
        public bool is_need { get; set; }
    }
}
